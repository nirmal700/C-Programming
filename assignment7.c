/* Program to swap the values of A and B*/
/*Name:- Nirmal Kumar Roll No:- C2_07*/
#include <stdio.h>
int main()
{
    int a,b,c;
    a=10;
    b=20;
    printf("the values of A and b are %i %i",a,b);
    c=a;
    a=b;
    b=c;
    printf("\nThe values after swapping are %i %i",a,b);
    return 0;
}
